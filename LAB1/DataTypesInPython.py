# Integer operations
a = 10
b = 3
print("Integer Addition:", a + b)
print("Integer Division:", a // b)
print("Integer Multiplication:", a * b)
print("Integer Modulo:", a % b)
# Float operations
x = 5.5
y = 2.0
print("Float Addition:", x + y)
print("Float Division:", x / y)
print("Float Multiplication:", x * y)
print("Float Modulo:", x % y)
# String operations
str1 = "Hello"
str2 = "World"
print("String Concatenation:", str1 + " " + str2)
print("String Repetition:", str1 * 3)
print("String Slicing:", str1[1:4])
# List operations
my_list = [1, 2, 3, 4]
print("Original List:", my_list)
my_list.append(5)
print("After Append:", my_list)
my_list.remove(2)
print("After Remove:", my_list)
print("List Slicing:", my_list[1:3])
# Tuple operations
my_tuple = (10, 20, 30, 40)
print("Tuple:", my_tuple)
print("Tuple Indexing:", my_tuple[2])
print("Tuple Slicing:", my_tuple[1:3])
# Set operations
set1 = {1, 2, 3}
set2 = {3, 4, 5}
print("Set Union:", set1 | set2)
print("Set Intersection:", set1 & set2)
print("Set Difference:", set1 - set2)
# Dictionary operations
my_dict = {'name': 'Gunveer Singh', 'age': 19}
print("Original Dictionary:", my_dict)
my_dict['age'] = 20
print("Updated Dictionary:", my_dict)
my_dict['city'] = 'Jammu'
print("Added New Key:", my_dict)
